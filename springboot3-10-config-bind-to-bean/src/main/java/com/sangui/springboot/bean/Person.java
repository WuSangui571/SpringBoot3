package com.sangui.springboot.bean;


import org.springframework.stereotype.Component;

/**
 * @Author: sangui
 * @CreateTime: 2025-04-26
 * @Description: Person
 * @Version: 1.0
 */
//@Component
public class Person {
    private String name;
    private int age;

    @Override
    public String toString() {
        return "Person{" +
                "name='" + name + '\'' +
                ", age=" + age +
                '}';
    }

    public Person() {
    }

    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
